def fill_placeholders(prompt: str, ask) -> str:
    """แทน {ตัวแปร} โดยเรียก ask(ชื่อตัวแปร) ทีละตัว"""
    import re
    result = prompt
    for var in re.findall(r"\{([^}]+)\}", prompt):
        value = ask(var)
        result = result.replace("{" + var + "}", value)
    return result
